// for loop
// Executes a block of code for a specified number of times

for (let i=0; i<5 ; i++){
    console.log(i);
}

// while loop-entry control loop
let count = 0;
while (count < 5) {
    console.log(count);
    count++;
}
//  do while-exit control loop
let Count = 0;
do {
    console.log(Count);
    Count++;

}while (Count < 5);
//for...in loop-Key is iterator
let person={name: "John", age: 30,occupation: "Developer"
};
for(let key in person) {
    console.log(key + ": " + person[key]);
}















